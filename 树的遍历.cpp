/**
 * 树的遍历只有三种：
 *      先根遍历 -> 先访问根节点，在一次遍历其子树
 *      后根遍历 -> 先访问完所有子树，再访问根节点
 *      层次遍历 -> 通过队列来一层一层遍历树
 */


/**
 * 求树的先根遍历，可以将树转化成二叉树（孩子兄弟表示法），求二叉树的先序遍历即可
 * 求树的后根遍历，可以将树转化成二叉树（孩子兄弟表示法），求二叉树的中序遍历即可
 */

